/*******************************************************************************
 *
 * 函数名  : zynq_gpio_init
 * 描述    : 初始化gpio
 * 输入参数: gpio - gpio结构
 *           pin  - gpio编号
 *           dir  - gpio方向
 * 输出参数: 无
 * 返回值:   无
 * 调用关系: 无
 * 其 它:    无
 *
 ******************************************************************************/
extern void zynq_gpio_init(int pin, int dir);

/*******************************************************************************
 *
 * 函数名  : zynq_gpio_exit
 * 描述    : 反初始化gpio
 * 输入参数: gpio - gpio结构
 * 输出参数: 无
 * 返回值:   无
 * 调用关系: 无
 * 其 它:    无
 *
 ******************************************************************************/
extern void zynq_gpio_exit(int pin);

/*******************************************************************************
 *
 * 函数名  : zynq_gpio_write
 * 描述    : gpio赋值
 * 输入参数: gpio - gpio结构
 *           val  - gpio值(0,1)
 * 输出参数: 无
 * 返回值:   无
 * 调用关系: 无
 * 其 它:    无
 *
 ******************************************************************************/
extern void zynq_gpio_write(int pin, int val);

/*******************************************************************************
 *
 * 函数名  : zynq_gpio_read
 * 描述    : gpio读值
 * 输入参数: gpio - gpio结构
 * 输出参数: 无
 * 返回值:   -1 失败
 *            0 低电平
 *            1 高电平
 *
 * 调用关系: 无
 * 其 它:    无
 *
 ******************************************************************************/
extern int zynq_gpio_read(int pin);


